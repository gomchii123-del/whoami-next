/**
 * compat-narratives.ts
 * 궁합 리포트 전용 내러티브 라이브러리
 * 통합수리 동역학(IND) 소스 기반 — 파동 간섭(Wave Interference), 에너지 변환 효율 반영
 */

export interface CompatNarrative {
    interferenceProps: {
        title: string;
        type: 'Synergy' | 'Collapse' | 'Orthogonal'; // 보강, 상쇄, 직교
        subtitle: string;
        energyEfficiency: string;
        description: string[];
    };
    relationshipDynamics: string[]; // 관계 동역학 (연인/배우자 관점)
    businessDynamics: string[];     // 비즈니스/사회적 관점 결합도
    solution: string[];             // 마찰 제어 및 솔루션
}

/**
 * 두 사람의 운명수(Life Path Number)의 위상차(Phase Difference)를 계산합니다.
 * ΔN = |N1 - N2| % 9
 * 반환값에 따라 관계의 물리적 형태가 결정됩니다.
 */
export function getCompatNarrative(n1: number, n2: number): CompatNarrative {
    // 1. 마스터 넘버 평탄화 (11->2, 22->4, 33->6)
    const base1 = n1 === 11 ? 2 : n1 === 22 ? 4 : n1 === 33 ? 6 : n1;
    const base2 = n2 === 11 ? 2 : n2 === 22 ? 4 : n2 === 33 ? 6 : n2;

    const diff = Math.abs(base1 - base2) % 9;

    // 공명 (Synergy) - 보강 간섭
    if (diff === 0 || diff === 3 || diff === 6) {
        return {
            interferenceProps: {
                title: '에너지 보강 간섭 (Synergy)',
                type: 'Synergy',
                subtitle: '주파수 대역이 일치하는 공명 상태',
                energyEfficiency: '150% 이상',
                description: [
                    "두 사람의 에너지가 만났을 때 서로를 소모시키는 것이 아니라 증폭시키는 '보강 간섭'이 발생합니다. 비슷한 가치관이나 속도를 가지고 있어 굳이 긴 설명이 필요 없는 관계입니다.",
                    "한 사람이 1을 주면 상대는 이를 1.5로 받아들입니다. 함께 있을 때 이유 없이 편안함을 느끼며, 서로의 부족한 점을 채우려는 노력보다 강점을 함께 밀어주는 방향으로 에너지가 흐릅니다."
                ]
            },
            relationshipDynamics: [
                "감정적 주파수가 강하게 공명합니다. 다른 사람들에게는 설명하기 힘든 서로만의 고유한 리듬과 언어가 존재합니다. 초기부터 강하게 끌리거나, 오랜 친구처럼 편안함을 느낄 수 있습니다.",
                "가장 큰 장점은 '안정감'입니다. 상대방이 무슨 생각을 하는지 비교적 투명하게 이해할 수 있어, 감정적 오해로 인한 소모전이 적습니다. 세상을 바라보는 눈높이와 방향성이 일치합니다."
            ],
            businessDynamics: [
                "일이나 목표를 함께 할 때 엄청난 부스트 효과가 있습니다. 동일한 방향을 보고 달릴 때 브레이크 없이 속도를 낼 수 있는 최적의 파트너십입니다.",
                "단, 둘 다 같은 맹점을 가질 수 있습니다. 서로가 놓치고 있는 리스크를 상대방도 함께 놓칠 위험이 있으니, 중요한 결정을 할 때는 제3자의 객관적 시각을 한 번쯤 빌리는 것도 좋습니다."
            ],
            solution: [
                "두 사람의 관계는 이미 훌륭한 공진 상태에 있습니다. 굳이 상대를 바꾸려 할 필요가 없으며, 지금의 속도를 유지하는 것만으로도 좋은 결과가 나옵니다.",
                "너무 통하기 때문에 오히려 관계가 안주하거나 지루해질 수 있습니다. 정기적으로 새로운 자극(여행, 새로운 취미, 새로운 도전)을 투입해 주면, 보강 간섭의 폭이 더욱 거대해집니다."
            ]
        };
    }
    // 직교 보완 (Orthogonal) - 상호 독립적 지지
    else if (diff === 2 || diff === 5 || diff === 8) {
        return {
            interferenceProps: {
                title: '직교 보완 (Orthogonal Support)',
                type: 'Orthogonal',
                subtitle: '침범하지 않고 서로의 구조를 완성하는 상태',
                energyEfficiency: '100% ~ 120%',
                description: [
                    "두 사람의 에너지는 평행선을 파고들지 않고, 수직으로 만나 서로의 지지대가 되어줍니다. 나와 완전히 다른 성향의 사람임에도 불구하고 이상하게 톱니바퀴가 맞아떨어지는 '구조적 결합'입니다.",
                    "서로 영역을 침범하거나 강요하지 않습니다. 내가 못하는 것을 상대방이 일상처럼 쉽게 해내고, 상대방이 어려워하는 것을 내가 커버해주는 각자의 역할 분담이 확실합니다."
                ]
            },
            relationshipDynamics: [
                "'나와 다르기 때문에 끌리는' 가장 전형적인 관계 패턴입니다. 서로의 성향이 교차하지 않기 때문에 감정적으로 심하게 부딪히기보다는, 서로의 차이를 인정하고 존중하게 됩니다.",
                "각자의 고유한 영역과 혼자만의 시간을 지켜주는 것이 가장 중요합니다. 상대방을 내 방식대로 통제하려는 순간부터 이 견고한 구조는 삐걱거리기 시작합니다. 거리를 둘수록 신뢰가 쌓이는 독특한 연대감이 있습니다."
            ],
            businessDynamics: [
                "비즈니스 파트너로서 가장 이상적인 효율을 자랑합니다. 기획자와 실행자, 혹은 창작자와 관리자처럼 역할 구분을 명확히 했을 때 나오는 퍼포먼스가 압도적입니다.",
                "업무 프로세스에서 서로의 방식이 이해가 가지 않을 수 있습니다. 하지만 결과물이 목적에 부합한다면, 중간 과정에 대해 불필요한 태클을 걸지 않는 것이 이 파트너십의 성공 공식입니다."
            ],
            solution: [
                "상대방이 왜 저렇게 행동하는지 이해하려 애쓰지 마세요. 이해하는 것이 아니라 '인정'하는 것이 이 관계의 핵심 룰입니다. 서로 다름을 인정할 때 에너지가 가장 효율적으로 돕니다.",
                "공동의 목표와 명확한 역할 분담만 합의되어 있다면, 그 어떤 어려움도 분담해서 이겨낼 수 있는 견고한 십자 가교가 형성되어 있습니다."
            ]
        };
    }
    // 상쇄 간섭 (Collapse) - 마찰과 극복의 과제
    else {
        return {
            interferenceProps: {
                title: '에너지 상쇄 간섭 (Collapse)',
                type: 'Collapse',
                subtitle: '마찰을 통해 새로운 구조를 짜야 하는 상태',
                energyEfficiency: '50% ~ 80% (마찰 제어 전)',
                description: [
                    "두 분의 만남은 서로의 에너지가 충돌하며 마찰열을 발생시키는 '상쇄 간섭' 구간에 있습니다. 이 관계는 서로의 성장을 위한 강력한 자극이자, 삶의 큰 과제를 던져주는 숙명적인 만남입니다.",
                    "초기 결합 시에 엄청난 열정으로 끌리지만, 시간이 지날수록 내가 보내는 신호가 상대방에게는 다른 의미로 해석되는 주파수 왜곡 현상을 경험하게 됩니다. 에너지가 엉키는 것을 풀어내는 기술이 필요합니다."
                ]
            },
            relationshipDynamics: [
                "사랑의 크기가 문제가 아니라 '표현 방식의 통역 오류'가 잦습니다. 나는 배려라고 한 행동이 상대에게는 간섭으로 느껴지고, 상대의 침묵이 나에게는 무관심으로 해석되기 쉽습니다.",
                "하지만 이 마찰을 회피하지 않고 조율점(Zero-Point)을 찾는 데 성공하면, 비온 뒤 땅이 굳어지듯 그 어떤 관계보다 견고해집니다. 이 관계의 목적은 상대방을 통해 '나의 한계를 깨고 확장하는 것'입니다."
            ],
            businessDynamics: [
                "가치관과 리스크를 바라보는 관점이 정반대일 확률이 높습니다. 의사 결정 과정에서 반드시 마찰이 발생하지만, 이를 긍정적으로 이용하면 조직 내 '완벽한 리스크 검증 시스템'이 될 수 있습니다.",
                "감정적인 서운함을 배제하고, 철저하게 계약서, 규정, 객관적 지표를 통해서만 서로 소통하도록 시스템을 구축해야 합니다. 경계를 명확히 긋지 않으면 에너지가 크게 누수됩니다."
            ],
            solution: [
                "상대방의 행동 패턴을 '나를 공격하거나 무시해서'라고 해석하는 본능적인 방어기제를 내려놓아야 합니다. 상대의 주파수가 나와 다를 뿐, 악의가 없음을 머리로 먼저 이해하세요.",
                "다툼이 발생했을 때 결론을 바로 내려고 하지 마세요. 열이 올랐을 때 하는 말은 무조건 왜곡되어 전달됩니다. 반드시 물리적 거리를 두고 열을 식힌 뒤 텍스트로 정리해서 소통하는 마찰 제어 강령이 필수입니다."
            ]
        };
    }
}
