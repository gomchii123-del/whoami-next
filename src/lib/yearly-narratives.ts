/**
 * AAOD 모듈러-9 알고리즘 구동
 * @param pCore 본질수 (태어난 일자 기준, 마스터번호 보존 안 된 한자리 수)
 * @param alpha 운명수/Life Path (마스터번호 보존 전 한자리 수) => 실제로는 LifePath(1~9)를 입력받음
 * @param targetYear 타겟 연도 (예: 2024)
 */
export function calculateAAOD(pCoreBase: number, alphaBase: number, targetYear: number): AAODResult {
    // 1자리 수로 정규화
    const pCore = reduceToSingleStr(pCoreBase);
    const alpha = reduceToSingleStr(alphaBase);
    const yearRed = reduceToSingleStr(targetYear);

    // 1. 타겟 합성 주파수 도출
    const combinedEnergy = pCore + alpha + yearRed;
    const nTarget = mod9(combinedEnergy);

    // 2. 상쇄 간섭 임계점 (180도)
    const destructive1 = mod9(nTarget + 4);
    const destructive2 = mod9(nTarget + 5);

    // 3. 고속 스캐너 (1~9 기본 Base + 특수 Base 16, 22 스캔)
    const synergy = [];
    const conflict = [];

    const keysToScan = [1, 2, 3, 4, 5, 6, 7, 8, 9, 16, 22];

    for (const baseNum of keysToScan) {
        const baseData = BaseDictionary[baseNum];
        if (!baseData) continue;

        const baseMod = mod9(baseNum);

        // 보강 간섭 (0도)
        if (baseMod === nTarget) {
            synergy.push({
                base: baseNum,
                keyword: baseData.keyword,
                manifestation: `[${baseData.keyword}] 성향의 인물/사건과 결합하여 큰 시너지 증폭`,
                story: baseData.synergyStory
            });
        }
        // 상쇄 간섭 (180도)
        else if (baseMod === destructive1 || baseMod === destructive2) {
            conflict.push({
                base: baseNum,
                keyword: baseData.keyword,
                manifestation: `[${baseData.keyword}] 성향의 유혹/인물과 충돌하여 시스템 붕괴 위험`,
                story: baseData.conflictStory
            });
        }
    }

    // 4. 수치 기반 전략 가이드 도출
    // nTarget(주파수)에 따른 테마 분류
    let theme = '';
    let guide: string[] = [];
    let caution: string[] = [];

    if ([1, 2, 4, 5, 7, 8].includes(nTarget)) {
        theme = '물질적 순환 및 행동 중심기';
        guide = [
            `올해의 합성 주파수는 ${nTarget}이며, 당신의 에너지가 내부보다 '외부 세계'를 향해 팽창하는 물리적 순환기로 돌입했습니다.`,
            '머릿속에서 계산만 하던 서류를 구체적인 행동과 서명으로 옮겨야 할 타이밍입니다. 지인들과의 비즈니스 논의가 예상보다 크게 발전상으로 나타나며, 현금과 명예의 물리적 이동이 매우 거세질 것입니다.',
            '강력한 보강 간섭이 들어오는 분야를 적극적으로 찾아내 집중 투자하시고, 망설이지 말고 자신의 지분을 분명히 요구하십시오.'
        ];
        caution = [
            '에너지가 외부로 쏠려 있는 만큼, 지나치게 세속적인 통제권 획득에만 몰두할 경우 이 상승기의 끝자락에 도달했을 때 극심한 내면적 공허함이나 인간적인 지탄을 맞을 수 있습니다.',
            '들어온 물리적 이득만큼 가족과 건강 등 시스템의 후방 부대를 챙기는 "안전핀"을 뽑지 않도록 극도로 유의해야 합니다.'
        ];
    } else {
        // 3, 6, 9 축
        theme = '정신적 변곡점 및 의지적 개입기';
        guide = [
            `금년도의 연산 결과 도출된 기준 주파수는 ${nTarget}입니다. 이는 물질적 확장을 뜻하기보다 '정신적인 진화와 관계의 재설정'을 요구하는 강력한 의지적 변곡점을 뜻합니다.`,
            '숫자들의 화려한 팽창보다, 내면의 불순물을 정화하고 인간관계 속에서 발생했던 오해의 매듭을 조율하는 데 주력하십시오. 물리적인 명상이나 내면 성찰의 시간이 당신의 오라(Aura)를 한껏 맑게 정돈해 줄 것입니다.',
            '비우고 나누는 타인을 향한 헌신이 이 시기에는 가장 높은 복리의 정신적 이자로 되어 돌아옵니다.'
        ];
        caution = [
            '이 타이밍에 물질에 집착하여 타인과 불필요한 마찰(Friction)을 일으키려 든다면, 그것이 차가운 카르마의 부채로 묶이게 되어 다음 주기의 성장과 확장을 근본적으로 억압하게 됩니다.',
            '용서할 사람은 용서하고, 내려놓아야 할 집착은 확실하게 쓰레기통에 던져버리십시오. 마음이 무거우면 우주가 주는 보상을 받아낼 여유조차 생기지 않습니다.'
        ];
    }

    return {
        targetYear,
        targetFrequency: nTarget,
        synergy,
        conflict,
        actionStrategy: {
            theme,
            guide,
            caution
        }
    };
}
