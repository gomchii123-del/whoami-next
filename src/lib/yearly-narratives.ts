// 모듈러 9 헬퍼 (나머지가 0이면 9로 반환)
function mod9(value: number): number {
    const remainder = value % 9;
    return remainder === 0 ? 9 : remainder;
}

// 문자열이나 숫자의 모든 자릿수를 1자리로 합산하는 헬퍼 (마스터넘버 보존 안함)
function reduceToSingleStr(val: number | string): number {
    let n = typeof val === 'string' ? val : val.toString();
    while (n.length > 1) {
        n = n.split('').reduce((acc, curr) => acc + parseInt(curr, 10), 0).toString();
    }
    return parseInt(n, 10);
}

