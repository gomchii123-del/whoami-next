/** Score_Total 값에 따라 적절한 메시지 키를 반환 */
export function getINDScoreKey(score: number): string {
    if (score >= 0.85) return 'golden';
    if (score >= 0.7) return 'cruise';
    if (score >= 0.5) return 'balance';
    if (score >= 0.3) return 'interference';
    return 'stagnation';
}

